<p>Here are some ways you can get involved and help people living with Type 1.</p>

<h3>Donate</h3>
<ul>
  <li><a href="https://www.breakthrought1d.org/" target="_blank">Breakthrough T1D (formerly JDRF)</a>, a leading non-profit organization working to find a cure for Type 1.</li>
  <li><a href="https://www.t1international.com" target="_blank">T1International</a>, a non-profit organization working to ensure all people with Type 1 have access to the medication and supplies they need to survive.</li>
</ul>

<h3>Advocacy</h3>
<p>Every person with Type 1 deserves affordable access to insulin and the devices that that would drastically improve their lives and health outcomes. Many people with Type 1 are forced to ration their insulin, and these devices are often not covered by insurance or require people to pay thousands out of pocket each year.</p>
<p>Here are some ways to help advocate for change:</p>

